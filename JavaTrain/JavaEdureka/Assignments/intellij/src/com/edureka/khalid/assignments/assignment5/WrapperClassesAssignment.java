package com.edureka.khalid.assignments.assignment5;

public class WrapperClassesAssignment {
    public static void main(String[] args) {

        System.out.println("Byte wrapper class");
        Byte byteWrapperType=12;//autoboxing
        System.out.println(byteWrapperType);
        byte bytePrimitiveType=byteWrapperType;//unboxing
        System.out.println(bytePrimitiveType);

        System.out.println("Short wrapper class");
        Short shortWrapperType=12;//autoboxing
        System.out.println(shortWrapperType);
        short shortPrimitiveType=shortWrapperType;//unboxing
        System.out.println(shortPrimitiveType);

        System.out.println("Long wrapper class");
        Long longWrapperType=12l;//autoboxing
        System.out.println(longWrapperType);
        long longPrimitiveType=longWrapperType;//unboxing
        System.out.println(longPrimitiveType);

        System.out.println("Float wrapper class");
        Float floatWrapperType=12F;//autoboxing
        System.out.println(floatWrapperType);
        float floatPrimitiveType=floatWrapperType;//unboxing
        System.out.println(floatPrimitiveType);

        System.out.println("Double wrapper class");
        Double doubleWrapperType=12D;//autoboxing
        System.out.println(doubleWrapperType);
        double doublePrimitiveType=doubleWrapperType;//unboxing
        System.out.println(doublePrimitiveType);

        System.out.println("Character wrapper class");
        Character characterWrapperType='A';//autoboxing
        System.out.println(characterWrapperType);
        double CharacterPrimitiveType=characterWrapperType;//unboxing
        System.out.println(CharacterPrimitiveType);


        System.out.println("Boolean wrapper class");
        Boolean booleanWrapperType=true;//autoboxing
        System.out.println(booleanWrapperType);
        boolean booleanPrimitiveType=booleanWrapperType;//unboxing
        System.out.println(booleanPrimitiveType);






    }
}
